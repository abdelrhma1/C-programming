#include <stdio.h>
/* Write a code that scan 3 numbers from the user
and print them in reversed order*/
int main()
{
    int x,y,z;
    printf("Please enter number_1: ");
    scanf("%d",&x);
    printf("Please enter number_2: ");
    scanf("%d",&y);
    printf("Please enter number_3: ");
    scanf("%d",&z);
    printf("number_3:%d\n",z);
    printf("number_2:%d\n",y);
    printf("number_1:%d\n",x);
}